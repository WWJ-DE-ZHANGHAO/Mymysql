//B+树索引数据结构
/*
* Mysql索引数据结构对经典的B+Tree进行了优化，在原B+Tree的基础上，增加了一个指向相邻叶子节点的链表指针，就形成带有顺序指针的B+树
* 提高访问区间访问的性能，叶子节点是双向循环链表，每个节点都存储了前驱和后继指针，首尾叶子节点相互链接，从而形成双向循环链表
* 此时就不用从根节点到叶子节点进行一次查询了，直接通过指针就可以访问相邻的叶子节点了
* 例如:最大度数为5的B+树:
* 一个节点存储了123、345、1200、1567这四个key之后，此时再存储一个key为1000，超过了5个指针，此时会进行分裂，
* 将中间key:1000存储在父节点中，并更新父节点的指针，123和345放在一个子节点，1000、1200和1567放在一个子节点
* 注意！！！
* 所有的节点都是存储在数据块/页中的
*    1000(两个指针，分别是大于等于1000和小于1000的指针)
*             /       \
*      -->123  345  <->   1000  1200 1567<-
*      |----------------------------------|
* 注意！！
* B+tree树的叶子节点存储key和数据，非叶子节点存储key和指针
* */
//Hash索引数据结构
/*
* 哈希索引就是采用一定的哈希算法，将键值换算成新的hash值映射到对应的槽位上然后存储在hash表上，
* 注意！！
* 如果计算出来的hash值相同，就会发生哈希冲突，此时会将这些键值存储在同一个槽位上，形成一个链表结构
* 特点:
* 1、Hash索引只能用于对等比较(=、in)，不支持范围查询(<、>、between)
* 因为哈希算法会将键值换算成一个新的hash值，无法根据hash值进行范围查询
* 2、无法利用索引完成排序操作
* 3、查询效率高，通常只需要一次检索就可以了，效率通常要高于B+树索引
* 步骤:
* 插入数据后，先将要索引的字段的值换算成新的哈希值，根据内部的哈希函数用字段的哈希值计算出插槽值，从而判断它该落在哈希表的哪个插槽上
* 注意！！！
* 在Mysql中支持hash索引的存储引擎有:Memory引擎，而InnoDB中具有自适应hash功能
* (mysql会根据我们查询条件。在指定的条件下会自动的将B+树索引构建出哈希)。即hash索引是存储引擎根据B+树索引在指定条件下自动构建的
* */
//索引结构面试题
/*
* 为什么InnoDB存储引擎选择B+树索引结构,而不是采用二叉树或者时红黑树或者B-tree?
* 不用二叉树是因为如果插入的数据时顺序插入的会形成链表，查询效率会降低，且数据量越大，层越深，查询效率也会越低
* 红黑树虽然可以避免链表，但是会增加树的高度，查询效率也会降低
* B-tree虽然可以避免链表和树高度，但是它的节点都是存储在块/页中的，一页最多存储16kb的数据
* 其无论时叶子节点还是非叶子节点都会存储数据，导致页中能存储的键值减少，指针也就减少了。保存同样多的数据，只能增加树的高度，导致性能降低
*
* 而B+树索引结构，由于B+树索引结构是存储在数据块/页中的，且数据只存储在叶子节点中，
* 所以页中能存储的键值会增加，指针也会增加(即最大度数更大)，从而提高查询效率，树的高度也会越低，查询效率也会越高
  且B+树中的叶子节点形成了一个双向循环链表，更加便于范围搜索和排序

 那为什么不用Hash索引？
 因为Hash索引只支持等值匹配，不支持范围查询和排序
*
* */

//索引分类
/*
*索引分类:主键索引、唯一索引、常规索引、全文索引
* 主键索引:针对表中主键创建的索引，默认自动创建，只有一个，关键词:PRIMARY
* 唯一索引:避免同一个表中某数据列中的值重复，可以有多个，关键词:UNIQUE。当为某个字段设置为唯一约束时，会自动为该字段生成唯一索引
* 常规索引:快速定位特定数据。可以有多个
* 全文索引:全文索引查找的是文本中的关键词，而不是比较索引中的值，可以有多个，关键词:FULLTEXT
*
* 而在InnoDB存储引擎中，根据索引的存储形式，又分为两种:
* 聚集索引(Clustered Index):将数据存储与索引放到一块，索引的结构的叶子节点保存了各个字段值以及对应的行数据，
* 特点:必须有，而且只有一个
* 二级索引(Secondary Index)也称为辅助索引或非聚集索引:将数据与索引分开存储，索引结构的叶子节点关联的是各个字段值以及对应的主键，
* 特点可以存在多个
* 如果存在主键，主键索引就是聚集索引
* 如果不存在主键，将使用第一个唯一(UNIQUE)索引作为聚集索引
* 如果既没有主键也不存在唯一索引，那么InnoDB会自动生成一个rowid作为隐藏的聚集索引
*
* SQL语句底层是使用回表查询，先查询二级索引找到对应的主键，再根据对应的主键去主键索引结构中找到对应的行数据
* 例如:select * from user where name ='Arm'
* name是二级索引，先根据name值找到对应的主键ID，随后通过ID的聚集索引找到对应的行数据
*
* 总结:
* 如果是聚集索引，B+tree的叶子节点保存的是完整的行数据(只是将主键放在上面，下面挂着该行的其他数据。方便找)，
* 聚集索引也称为聚簇索引
* 如果是二级索引，B+tree的叶子节点保存的是索引列的值+(聚簇索引主键值)主键字段值
* */

