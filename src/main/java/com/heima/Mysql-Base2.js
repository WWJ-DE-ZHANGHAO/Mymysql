//存储引擎
/*
* Mysql服务器分为连接层、服务层、引擎层、储存层
* 连接层用于登录Mysql服务的认证工作
* 服务层包括SQL接口、解析器、查询优化器和缓存
* 引擎层有很多种引擎，如果没有想要的还可以手动加所以也称为可插拔式存储引擎
* 存储层包含系统文件和文件日志
* Mysql5.5版本之后默认使用的引擎是InnoDB引擎，在此之前的默认引擎是MyISAM引擎
*
* 存储引擎:就是存储数据、建立索引、更新/查询数据等技术的实现方式。存储引擎是基于表的，而不是基于库的，所以存储引擎也可被称为表类型
* 也就是说同一个数据库中不同的表是可以选择不同的存储引擎对的。
* 在建表的时候可以指定存储引擎，如果没指定的话会使用默认的存储引擎InnoDB
*
* 指定存储引擎的命令:
* Create Table 表名(
*  字段名 字段类型[comment 字段注释]
* ……………………
* )ENGINE=INNODB [COMMENT 表注释]; //使用ENGINE指定存储引擎
*
* 查看数据库支持哪些存储引擎的命令
* show engines;
* 会显示哪些引擎支持哪些不支持，以及引擎的注释、对事务是否支持、是否支持XA协议和是否支持保存点
*例如:MEMEORY引擎、MyISAM引擎、InnoDB引擎等等
*
* */

//InnoDB存储引擎
/*
* InnoDB是一种兼顾高可靠性和高性能的通用存储引擎。
* 特点:事务、外键、行级锁
* DML操作遵循ACID模型，支持事务
* 行级锁，提高并发访问性能
* 支持外键FOREIGN KEY约束，保证数据的完整性和正确性
* InnoDB的文件都是xxx.ibd格式的，xxx就是表名。InnoDB引擎的每张表都对应这样一个表空间文件，
* 存储该表的表结构(frm，sdi)、数据和索引。以及一个参数innodb_file_per_table的值，
* 来决定是一张表对应一个表空间还是多张表共有一个表空间
* ON表示一个表对应一个表空间，OFF表示多张表共享一个表空间
*
* 查看系统变量的值命令:
* show variables like 'innodb_file_per_table'; //模糊匹配要查询的变量的名称
*
* InnoDB引擎的逻辑存储结构:
* Innodb引擎的逻辑存储结构包括表空间、段、区、页和行
* Table Space(表空间):表空间是InnoDB引擎的最高级别的逻辑存储结构，表空间中包含了段、区、页和行等其他的逻辑存储结构，
* ibd文件就是InnoDB引擎的表空间文件
* Segment(段):段是表空间中的一个逻辑存储结构，段中包含了区、页和行等其他的逻辑存储结构
* Extent(区):区是段中的一个逻辑存储结构，区中包含了页和行等其他的逻辑存储结构，能存储1MB的数据记录和索引记录即64页
* Page(页):页是区中的一个逻辑存储结构，页中包含了行等其他的逻辑存储结构，能存储16KB的数据记录和索引记录
* Row(行):行是页中的一个逻辑存储结构，行中包含了数据记录和字段
*
*
* */

